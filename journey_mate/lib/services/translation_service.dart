/// Static translation lookup (ts = "translation static")
/// Use this for hardcoded keys ported from FlutterFlow
String ts(BuildContext context, String key) {
  // Get current locale from Flutter's built-in system
  final locale = Localizations.localeOf(context);
  final languageCode = locale.languageCode;

  // Look up translation
  final translations = kStaticTranslations[key];
  if (translations == null) {
    debugPrint('⚠️ ts: Unknown key "$key"');
    return key;
  }

  // Try current language, fall back to English
  final text = translations[languageCode] ?? translations['en'] ?? key;

  if (text == key && languageCode != 'en') {
    debugPrint('⚠️ ts: No translation for "$key" in $languageCode or en');
  }

  return text;
}

/// Dynamic translation lookup (td = "translation dynamic")
/// Use this for keys fetched from BuildShip API (stored in translationsCacheProvider)
/// Requires WidgetRef to watch the provider
String td(WidgetRef ref, String key) {
  final cache = ref.watch(translationsCacheProvider);
  final text = cache[key];

  if (text == null) {
    debugPrint('⚠️ td: Missing dynamic key "$key"');
    return key;
  }

  return text;
}
