/// Icon Sizes - Standard icon size scale
///
/// Follows the same even-increment pattern as AppSpacing.
/// Use these instead of raw numeric icon sizes.
class AppIconSize {
  AppIconSize._();

  /// 8px - Tiny icons (filter chip indicators)
  static const double xs = 8.0;

  /// 12px - Small icons (chevrons, compact indicators)
  static const double sm = 12.0;

  /// 14px - Medium-small icons (action pill icons)
  static const double msm = 14.0;

  /// 16px - Standard icons (section headers, inline icons)
  static const double md = 16.0;

  /// 24px - Large icons (Material default, standalone actions)
  static const double lg = 24.0;
}
